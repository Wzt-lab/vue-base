// 数组和字符串转换方法
let arr = [1, 2, 3];
// toString
console.log(arr.toString());
// join
console.log(arr.join('-'));
// toLocalString
console.log(arr.toLocaleString());

/**
 * toString和toLocaleString
 * 1.相同点：都是转成字符串
 * 2.不同点
 *      toString：就是转换成字符串
 * */ 
let num = 1234456.78911

console.log('[ toString ]', num.toString())
console.log('[ toLocaleString ]', num.toLocaleString())


